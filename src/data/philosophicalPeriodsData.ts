export const getUnlockedPeriods = (completedCheckpoints: string[], totalCheckpoints: number): string[] => {
  const completionRate = completedCheckpoints.length / totalCheckpoints;
  const unlockedPeriods: string[] = [];
  
  // Unlock periods based on completion rate
  if (completionRate >= 0.1) unlockedPeriods.push('early-introduction');
  if (completionRate >= 0.2) unlockedPeriods.push('ly-dynasty-golden');
  if (completionRate >= 0.4) unlockedPeriods.push('tran-truc-lam');
  if (completionRate >= 0.6) unlockedPeriods.push('le-revival');
  if (completionRate >= 0.8) unlockedPeriods.push('nguyen-consolidation');
  if (completionRate >= 1.0) unlockedPeriods.push('modern-engaged');
  
  return unlockedPeriods;
};
